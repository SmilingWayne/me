result = document.getElementsByClassName('md-content')[0]; 
var h1 = result.getElementsByTagName("p")
var li = result.getElementsByTagName("li")

HanZiCount = 0

for(var i = 0; i < h1.length; i ++ ){
    HanZiCount += h1[i].innerText.length
}
for(var i = 0; i < li.length; i ++ ){
    HanZiCount += li[i].innerText.length
}

var wordcount = document.getElementById("hanzi");
var expectedreadingtime = document.getElementById("exprdt")

wordcount.innerHTML = HanZiCount
expectedreadingtime.innerHTML = Math.max(Math.floor(HanZiCount / 500) ,1)+ "分钟"
